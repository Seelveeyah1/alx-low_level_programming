/**
 * struct dog - Structure representing information about
 * a dog
 * @name: Pointer to the name of the dog
 * @age: Age of the dog
 * @owner: Pointer to the name of the dog's owner
 *
 * Description: This structure defines the attributes of a
 * dog including its name, age, and owner's name.
 */

typedef struct dog
{
	char *name;
	float age;
	char *owner;
} dog_t;

